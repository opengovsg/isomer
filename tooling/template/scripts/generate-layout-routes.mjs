import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

import { getHeavyLayoutRoutes } from "./utils/heavyLayouts.mjs"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const TEMPLATE_ROOT = path.join(__dirname, "..")

/** @type {Record<string, string>} */
const LAYOUT_PAGE_FACTORY = {
  collection: "makeCollectionPage",
  search: "makeSearchPage",
  database: "makeDatabasePage",
}

/** @type {Record<string, string>} */
const LAYOUT_PAGE_MODULE = {
  collection: "@/render/makeCollectionPage",
  search: "@/render/makeSearchPage",
  database: "@/render/makeDatabasePage",
}

/**
 * @param {string} normalizedPermalink
 * @param {string} layout
 */
function pageSource(normalizedPermalink, layout) {
  const factory = LAYOUT_PAGE_FACTORY[layout]
  const mod = LAYOUT_PAGE_MODULE[layout]
  if (!factory || !mod) {
    throw new Error(
      `[generate-layout-routes] no page factory for layout: ${layout}`,
    )
  }

  return `// GENERATED by scripts/generate-layout-routes.mjs — do not edit
// layout: ${layout}
import { ${factory} } from "${mod}"

export const dynamic = "force-static"

const { Page, generateMetadata } = ${factory}(${JSON.stringify(normalizedPermalink)})
export { generateMetadata }
export default Page
`
}

/**
 * @param {string} [baseDir]
 */
export function run(baseDir = TEMPLATE_ROOT) {
  const sitemapPath = path.join(baseDir, "sitemap.json")
  const heavyRoot = path.join(baseDir, "app", "(heavy)")
  const manifestPath = path.join(baseDir, ".generated-heavy-routes.json")

  if (fs.existsSync(manifestPath)) {
    /** @type {string[]} */
    const previous = JSON.parse(fs.readFileSync(manifestPath, "utf8"))
    for (const rel of previous) {
      const filePath = path.join(baseDir, rel)
      if (fs.existsSync(filePath)) fs.unlinkSync(filePath)
    }
    fs.unlinkSync(manifestPath)
  }
  if (fs.existsSync(heavyRoot)) {
    fs.rmSync(heavyRoot, { recursive: true, force: true })
  }

  if (!fs.existsSync(sitemapPath)) {
    console.warn(
      `[generate-layout-routes] No sitemap at ${sitemapPath}; skipping`,
    )
    return { written: [] }
  }

  const sitemap = JSON.parse(fs.readFileSync(sitemapPath, "utf8"))
  const routes = getHeavyLayoutRoutes(sitemap)

  /** @type {string[]} */
  const written = []

  for (const { normalized, layout } of routes) {
    const targetDir = path.join(heavyRoot, ...normalized.split("/"))
    const targetFile = path.join(targetDir, "page.tsx")
    fs.mkdirSync(targetDir, { recursive: true })
    fs.writeFileSync(targetFile, pageSource(normalized, layout), "utf8")
    written.push(path.relative(baseDir, targetFile))
  }

  fs.writeFileSync(manifestPath, JSON.stringify(written, null, 2), "utf8")
  console.log(
    `[generate-layout-routes] wrote ${written.length} heavy route(s):`,
    written.length ? written.join(", ") : "(none)",
  )
  return { written }
}

const isEntry = process.argv[1] === fileURLToPath(import.meta.url)
if (isEntry) {
  run()
}
